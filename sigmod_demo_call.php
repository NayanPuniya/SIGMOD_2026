      The SIGMOD demonstration program has become an important venue for sharing cutting-edge data management system prototypes (software, hardware, or both) with the greater SIGMOD community. The emphasis of the program is on visionary next-generation systems requiring significant research and development effort. Such demonstration papers are expected to focus on data-intensive components of data science pipelines and solve problems in areas of interest to the community, such as data curation, optimization, performance, storage, and systems.
    </p>

